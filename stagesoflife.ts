


let personAge: number = 64;

if (personAge < 2) {
    console.log("Baby")
} else if (personAge >= 2 && personAge < 4) {
    console.log("Toddler")
} else if (personAge >= 4 && personAge < 13) {
    console.log("Kid")
} else if (personAge >= 13 && personAge < 20) {
    console.log("Teenager")
} else if (personAge >= 20 && personAge < 65) {
    console.log("Adult")
} else {
    console.log("Elder")
}


